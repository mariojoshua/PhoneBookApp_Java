package com.uttara.mvc.contactsApp;

/**
 * This is a placeholder interface for all constants of the application.
 * It has only variables with fixed values that 
 * can be used throughout the application code base.
 * @author mariojoshuaaugustine
 */
public interface Constants {

	/*
	 * Interface variables are static and final by default in java. They are static
	 * because java interfaces cannot be instantiated on their own. The final
	 * modifier ensures the value assigned to the interface variable is a true
	 * constant that cannot be re-assigned, hence interfaces can declare only
	 * constants, not instance variables.
	 */
	String SUCCESS = "success";
	String UNIQUE = "unique";
	String FAILURE ="failure";
}
